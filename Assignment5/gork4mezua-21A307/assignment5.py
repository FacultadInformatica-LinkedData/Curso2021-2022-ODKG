"""Some of these queries are simple and will run in the Wikidata endpoint. Try both of them using SPARQL and Kypher"""

# Which actors has Schwarzenegger worked with throughout his career? (Print also the movie)
# (in SPARQL)
sparql.setQuery('''
  SELECT ?actor_label ?actor_id ?movie_label ?movie_id 
  WHERE {
      ?movie_id wdt:P161 wd:Q2685 . # Q2685 (Arnold Schwarzenegger)
      ?movie_id wdt:P161 ?actor_id .
      ?actor_id rdfs:label ?actor_label .
      ?movie_id rdfs:label ?movie_label .
      FILTER(?actor_id != wd:Q2685) .
      FILTER(LANG(?actor_label) = 'en')
      FILTER(LANG(?movie_label) = 'en')
  }
  ORDER BY ?actor_label
''')
sparql.setReturnFormat(JSON)
results = sparql.query().convert()

# Result (SPARQL)
results_df = pd.io.json.json_normalize(results['results']['bindings'])
results_df[['actor_label.value', 'movie_label.value']].head()

# In Kypher:
kgtk("""
    query -i all \
     --match '(movies)-[:P161]->(:Q2685),
              (movies)-[:P161]->(actors)' \
     --return 'actors' 
    / add-labels
""")

