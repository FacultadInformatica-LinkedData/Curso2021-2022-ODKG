# Retrieve at least two members of Schwarzenegger's political party. Make sure only persons are returned
# SPARQL:
sparql.setQuery('''
  SELECT ?member_label ?member_id ?political_label ?political_id 
  WHERE {
      wd:Q2685 wdt:P102 ?political_id .
      ?member_id wdt:P102 ?political_id .
      ?member_id wdt:P31 wd:Q5 .
      ?member_id rdfs:label ?member_label .
      ?political_id rdfs:label ?political_label .
      FILTER(LANG(?member_label) = 'en') .
      FILTER(LANG(?political_label) = 'en')
  }
  ORDER BY ?member_label
  LIMIT 5
''')
